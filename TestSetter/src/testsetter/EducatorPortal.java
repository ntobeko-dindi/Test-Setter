     void getDetails()
    {
        String StuffN = null;
        try
        {
            try
            {
                Scanner read = new Scanner(new FileInputStream("C:\\TestSetter\\tempFile2.txt"));
                    StuffN = read.nextLine();
                    read.close();
            }
            catch(Exception ex){
            }
            
                String sql = "select * from FullStaffDetails where StuffNo='"+StuffN+"'";
                ResultSet data = new DataAccess().getData(sql);
               
                String name = null;
                String surname = null;
                if(data.next())
                {  
                    name = data.getString("Fname").toUpperCase();
                    surname = data.getString("Lname").toUpperCase();
                }
                String init = ""+name.charAt(0);
                for(int c = 1;c < name.length();c++)
                {
                    if(Character.isWhitespace(name.charAt(c)))
                    {
                        init += name.charAt(c+1);
                    }
                }
                init += '.';
                lblHeader.setText("Welcome "+init+" "+surname);
        }
        catch(Exception ex){}
    }
