    void getGrades(String testName)
    {  
        try
        {
            Connection conn=DriverManager.getConnection("jdbc:ucanaccess://C:\\TestSetter\\src\\testsetter\\systemDB.accdb");
            String sql="select * from ClassGrades where TestName='"+testName+"'";
            if(testName == null || testName.equals("Test Name e.g. Test 1"))
            {
                sql="select * from ClassGrades";
            }
            PreparedStatement pst=conn.prepareStatement(sql);
            ResultSet rs=pst.executeQuery();
            tblSudentMarks.setModel(DbUtils.resultSetToTableModel(rs));             
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }        
    }
